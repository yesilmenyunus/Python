# Girilen değeri sayı tipine dönüştürüp değişkenlere atıyoruz
sayı1 = float(input("İlk sayıyı giriniz: "))
sayı2 = float(input("İkinci sayıyı giriniz: "))

print("{} ve {} sayılarının toplamı: {}".format(sayı1,sayı2,sayı1+sayı2))
print("{} ve {} sayılarının farkı: {}".format(sayı1,sayı2,sayı1-sayı2))
print("{} ve {} sayılarının çarpımı: {}".format(sayı1,sayı2,sayı1*sayı2))
print("{} sayısını {} sayısına böldüğümüzde sonuç: {}".format(sayı1,sayı2,sayı1/sayı2))